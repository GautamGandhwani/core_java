package co.in.variableandoperator;

public class OtherDataTypeToString {
	public static void main(String[] args) {
		String str1=String.valueOf(5);
		System.out.println("Integer = "+str1);
		
		String str2=String.valueOf(5.5);
		System.out.println("Float = "+str2);
		
		String str3=String.valueOf(true);
		System.out.println("Boolean = "+str3);
		
		String str4=String.valueOf(8);
		System.out.println("Long = "+str4);
		
		String str5=String.valueOf(5.3);
		System.out.println("Double = "+str5);
	}
}
