package co.in.variableandoperator;

public class BigtoSmall {
	
	public static void main(String[] args) {
		
		double d=5.5;
		int i=(int)d;
		short s=(short)i;
		int a=10;
		float f=(float)i;
		
		System.out.println(d);
		System.out.println(i);
		System.out.println(s);
		System.out.println(a);
		System.out.println(f);
		
	}
}
